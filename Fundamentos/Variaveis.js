console.clear()

//variáveis
let nome //Trabalhada a variável String
let idade //Trabalhada a variável Number
let vip //Trabalhada a variável Boolean

nome = "Erica Viana" //quando escreve o nome dentro de aspas, ele cria a variável String
console.log(typeof(nome)) //atribui à variável "nome" um valor (typeof é para identificar qual o tipo de variável (String))
console.log(nome) //para escrever o conteúdo dessa variável configurada

idade = 31 //quando escreve a idade sem aspas, ele cria a variável Number
console.log(typeof(idade)) //atribui à variável "idade" um valor (typeof é para identificar qual o tipo de variável (Number))
console.log(idade) //para escrever o conteúdo dessa variável configurada

vip = true //quando escreve o vip true, ele cria a variável Booleana
console.log(typeof(vip)) //atribui à variável "vip" um valor (typeof é para identificar qual o tipo de variável (Boolean))
console.log(vip) //para escrever o conteúdo dessa variável configurada